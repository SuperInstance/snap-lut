"""
Generate BRAM snap lookup table for Pythagorean triple angle snapping.

Maps angles 0..2π in 1024 steps to the nearest Pythagorean triple angle.
Output: Verilog $readmemh hex file + C header for ESP32.

Q4.12 fixed point: 4 integer bits (sign+3) + 12 fractional bits.
Range: -8.0 to +7.999755859375, resolution ~0.000244140625
Angle 0..2π ≈ 0..6.2832 fits in unsigned 0..25737 (Q4.12)
"""

